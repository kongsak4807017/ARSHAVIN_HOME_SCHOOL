import fs from 'node:fs';
import vm from 'node:vm';
import assert from 'node:assert/strict';

const read = path => fs.readFileSync(path, 'utf8');
const lessonPath = 'subjects/environment/water-cycle-responsible-use.html';
const scriptPath = 'assets/js/water-cycle-lesson.js';
const worksheetPath = 'worksheets/student/water-cycle-responsible-use-a4.html';
const guidePath = 'worksheets/teacher-guides/water-cycle-responsible-use-guide.html';
const required = [lessonPath, scriptPath, worksheetPath, guidePath];
required.forEach(path => assert.ok(fs.existsSync(path), path));

const lesson = read(lessonPath);
const script = read(scriptPath);
const worksheet = read(worksheetPath);
const guide = read(guidePath);
const index = read('index.html');
const shell = read('assets/js/learning-shell.js');
const sw = read('service-worker.js');

new vm.Script(script, { filename: scriptPath });
assert.match(lesson, /<html lang="th">/);
assert.match(lesson, /<h1>[\s\S]*<br>[\s\S]*<span>/);
assert.match(lesson, /data-current-lesson="ENV-02"/);
assert.match(lesson, /evaporation/);
assert.match(lesson, /condensation/);
assert.match(lesson, /precipitation/);
assert.match(lesson, /watershed/);
assert.match(lesson, /CARE/);
assert.match(lesson, /สถานการณ์จำลอง/);
assert.match(lesson, /ไม่ยืนยันว่าตัวอย่างน้ำใดปลอดภัยสำหรับดื่ม/);
assert.match(lesson, /(ไม่|ห้าม)ชิมน้ำ/);
assert.match(lesson, /aria-live="polite"/);
assert.match(lesson, /<fieldset>/);
assert.match(script, /arshavin\.environment\.water\.v1/);
assert.match(script, /cycleComplete/);
assert.match(script, /watershedComplete/);
assert.match(script, /quizComplete/);
assert.doesNotMatch(script, /fetch\(|XMLHttpRequest|WebSocket|sendBeacon/);
assert.equal((worksheet.match(/class="worksheet(?:\s|\")/g) || []).length, 2);
assert.match(worksheet, /@page\{size:A4 portrait/);
assert.match(guide, /Rubric 4 ระดับ/);
assert.match(guide, /(ไม่|ห้าม)ชิมน้ำ(?:ทดลอง|จากแหล่งทดลอง)?/);
required.forEach(path => assert.ok(sw.includes(`./${path}`) || path === scriptPath && sw.includes('./assets/js/water-cycle-lesson.js'), path));
assert.match(sw, /arshavin-grade4-v1[01]/);
assert.ok(index.includes(lessonPath));
assert.ok(index.includes('arshavin.environment.water.v1'));
assert.ok(shell.includes("id: 'ENV-02'"));
assert.ok(shell.includes('cycleComplete && data.watershedComplete && data.quizComplete'));
console.log('ENV-02 focused static checks passed.');
