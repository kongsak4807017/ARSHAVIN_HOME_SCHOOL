assert.ok(home.includes(lesson));
assert.ok(home.includes('arshavin.maker.heat.v1'));
assert.ok(shell.includes("id: 'MAKER-09'"));
assert.ok(shell.includes('arshavin.maker.heat.v1'));
console.log('PASS MAKER-09 focused static checks');
