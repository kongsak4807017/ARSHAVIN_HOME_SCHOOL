assert.match(all,/convection|การพาความร้อน/i);
assert.match(all,/radiation|การแผ่รังสี/i);
assert.match(all,/insulator|ฉนวน/i);
assert.match(all,/ชะลอ.*ถ่ายโอน|slows? .*transfer/is);
assert.match(all,/fair test|การทดสอบอย่างยุติธรรม/i);
assert.match(all,/อุณหภูมิห้อง|room-temperature/i);
assert.match(all,/ห้าม.*เปลวไฟ|no flames?/is);
assert.match(all,/ห้าม.*น้ำเดือด|no boiling water/is);
