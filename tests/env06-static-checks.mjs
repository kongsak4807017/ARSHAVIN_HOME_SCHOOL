import fs from 'node:fs';
const read=p=>fs.readFileSync(p,'utf8');
const lesson=read('subjects/environment/climate-weather-responsible-preparedness.html');
const script=read('assets/js/climate-weather-lesson.js');
const worksheet=read('worksheets/student/climate-weather-responsible-preparedness-a4.html');
const guide=read('worksheets/teacher-guides/climate-weather-responsible-preparedness-guide.html');
const shell=read('assets/js/learning-shell.js');
const sw=read('service-worker.js');
const checks=[];
const ok=(name,condition)=>{checks.push([name,Boolean(condition)]);if(!condition)process.exitCode=1};
ok('bilingual title',lesson.includes('ภูมิอากาศ สภาพอากาศ')&&lesson.includes('Climate, Weather and Responsible Preparedness'));
ok('weather climate distinction',/หนึ่งวันเป็น weather/.test(lesson)&&/หลายสิบปี/.test(lesson)&&/one rainy day insufficient/.test(worksheet));
ok('READY framework',/Read the source/.test(lesson)&&/Yield to updates/.test(lesson));
ok('native keyboard controls',lesson.includes('<select id="hazard"')&&lesson.includes('type="radio"')&&lesson.includes('type="submit"'));
ok('accessible feedback',(lesson.match(/aria-live="polite"/g)||[]).length>=3&&(lesson.match(/tabindex="-1"/g)||[]).length>=3);
ok('local-only key',script.includes('arshavin.environment.climate.v1')&&script.includes('localStorage'));
ok('completion gates',script.includes('evidenceComplete')&&script.includes('preparednessComplete')&&script.includes('quizComplete'));
ok('no outbound APIs',!/(fetch\(|XMLHttpRequest|WebSocket|sendBeacon)/.test(script));
ok('privacy safeguards',/ห้ามกรอกที่อยู่/.test(lesson)&&/home coordinates/.test(worksheet)&&/โรค ยา รายได้/.test(guide));
ok('no hazard observation task',/ห้ามให้เด็กออกไปสังเกตน้ำหลาก/.test(guide)&&/ออกไปถ่ายวิดีโอ/.test(lesson));
ok('exactly two A4 worksheets',(worksheet.match(/class="worksheet"/g)||[]).length===2&&worksheet.includes('@page{size:A4'));
ok('teacher rubric',guide.includes('Rubric 4 ระดับ')&&guide.includes('AAC'));
ok('shell integration',shell.includes("id: 'ENV-06'")&&shell.includes('arshavin.environment.climate.v1'));
ok('offline integration',/arshavin-grade4-v\d+/.test(sw)&&sw.includes('climate-weather-responsible-preparedness.html')&&sw.includes('climate-weather-lesson.js'));
for(const [name,pass] of checks)console.log(`${pass?'PASS':'FAIL'} ${name}`);
if(process.exitCode)throw new Error('ENV-06 focused static checks failed');
