import fs from 'node:fs';
import path from 'node:path';
import vm from 'node:vm';
import assert from 'node:assert/strict';
const root=path.resolve(import.meta.dirname,'..');
const read=relative=>fs.readFileSync(path.join(root,relative),'utf8');
const lesson='subjects/citizenship/emergency-rumours-risk-communication-responsible-help.html';
const script='assets/js/emergency-rumours-lesson.js';
const worksheet='worksheets/student/emergency-rumours-risk-communication-responsible-help-a4.html';
const guide='worksheets/teacher-guides/emergency-rumours-risk-communication-responsible-help-guide.html';
const material=read(lesson)+read(worksheet)+read(guide);
new vm.Script(read(script),{filename:script});
assert.match(read(lesson),/<html lang="th">/);
assert.match(read(lesson),/data-current-lesson="CIT-09"/);
assert.match(material,/CHECK/);
assert.match(material,/source|ต้นทาง/i);
assert.match(material,/date|วัน/i);
assert.match(material,/time|เวลา/i);
assert.match(material,/place|พื้นที่/i);
assert.match(material,/known|สิ่งที่รู้/i);
assert.match(material,/unknown|ยังไม่รู้|ความไม่แน่นอน/i);
assert.match(material,/rumou?r|ข่าวลือ/i);
assert.match(material,/official|ทางการ/i);
assert.match(material,/ชื่อ.*พิกัด|names?.*locations?/is);
assert.match(material,/medical|ทางการแพทย์|ข้อมูลสุขภาพ/i);
assert.match(material,/หยุดบทเรียน|stop the lesson/i);
assert.match(material,/ผู้ใหญ่|adult/i);
assert.doesNotMatch(material,/draggable="true"/);
assert.doesNotMatch(read(script),/fetch\(|XMLHttpRequest|WebSocket|sendBeacon/);
['verificationComplete','helpComplete','quizComplete','arshavin.citizenship.rumours.v1'].forEach(value=>assert.ok(read(script).includes(value),value));
const sheet=read(worksheet);
assert.equal((sheet.match(/class="worksheet(?:\s|")/g)||[]).length,2);
assert.match(sheet,/@page\s*\{[^}]*size:\s*A4/is);
const index=read('index.html'),shell=read('assets/js/learning-shell.js'),sw=read('service-worker.js');
[lesson,script,worksheet,guide].forEach(file=>{assert.ok(index.includes(file)||file!==lesson,file);assert.ok(sw.includes(`./${file}`),file)});
assert.ok(shell.includes("id: 'CIT-09'"));
assert.ok(shell.includes('arshavin.citizenship.rumours.v1'));
assert.match(sw,/arshavin-grade4-v\d+/);
console.log('PASS CIT-09 focused static checks');