import fs from 'node:fs';
const read=p=>fs.readFileSync(p,'utf8');
const lesson=read('subjects/ai-science/recommendation-ranking-fair-choice.html');
const script=read('assets/js/recommendation-fair-choice-lesson.js');
const worksheet=read('worksheets/student/recommendation-ranking-fair-choice-a4.html');
const guide=read('worksheets/teacher-guides/recommendation-ranking-fair-choice-guide.html');
const shell=read('assets/js/learning-shell.js');
const sw=read('service-worker.js');
const checks=[];
const ok=(name,condition)=>{checks.push([name,Boolean(condition)]);if(!condition)process.exitCode=1};
ok('bilingual title',lesson.includes('ระบบแนะนำ')&&lesson.includes('Recommendation, Ranking and Fair Choice'));
ok('CLEAR framework',/Clarify the goal/.test(lesson)&&/Review fairness/.test(lesson));
ok('native controls',lesson.includes('<select id="priority">')&&lesson.includes('type="radio"')&&lesson.includes('type="button"'));
ok('accessible feedback',lesson.match(/aria-live="polite"/g)?.length>=3&&lesson.match(/tabindex="-1"/g)?.length>=3);
ok('local-only key',script.includes('arshavin.ai.recommendation.v1')&&script.includes('localStorage'));
ok('completion gates',script.includes('recommenderComplete')&&script.includes('fairnessComplete')&&script.includes('quizComplete'));
ok('no outbound APIs',!/(fetch\(|XMLHttpRequest|WebSocket|sendBeacon)/.test(script));
ok('no child profiling',/ห้ามใช้ระบบนี้จัดอันดับหรือทำนายคุณค่าของเด็ก/.test(lesson)&&/profiling/.test(guide));
ok('user control',/เปลี่ยนน้ำหนัก/.test(lesson)&&/ปิดคำแนะนำ/.test(worksheet));
ok('exactly two A4 worksheets',(worksheet.match(/class="worksheet"/g)||[]).length===2&&worksheet.includes('@page{size:A4'));
ok('teacher rubric',guide.includes('Rubric 4 ระดับ')&&guide.includes('AAC'));
ok('shell integration',shell.includes("id: 'AI-06'")&&shell.includes('arshavin.ai.recommendation.v1'));
ok('offline integration',/arshavin-grade4-v\d+/.test(sw)&&sw.includes('recommendation-ranking-fair-choice.html')&&sw.includes('recommendation-fair-choice-lesson.js'));
for(const [name,pass] of checks)console.log(`${pass?'PASS':'FAIL'} ${name}`);
if(process.exitCode)throw new Error('AI-06 focused static checks failed');
