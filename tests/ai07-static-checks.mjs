import fs from 'node:fs';
import vm from 'node:vm';
const read=p=>fs.readFileSync(new URL(`../${p}`,import.meta.url),'utf8');
const assert=(condition,message)=>{if(!condition)throw new Error(message);};
const lesson='subjects/ai-science/robots-sensors-fail-safe-systems.html';
const script='assets/js/robots-fail-safe-lesson.js';
const worksheet='worksheets/student/robots-sensors-fail-safe-systems-a4.html';
const guide='worksheets/teacher-guides/robots-sensors-fail-safe-systems-guide.html';
for(const path of [lesson,script,worksheet,guide])assert(fs.existsSync(new URL(`../${path}`,import.meta.url)),`Missing ${path}`);
const html=read(lesson),js=read(script),ws=read(worksheet),teacher=read(guide),sw=read('service-worker.js'),index=read('index.html'),shell=read('assets/js/learning-shell.js');
new vm.Script(js);
assert(html.includes('AI-07')&&html.includes('Input–Process–Output'),'Missing bilingual AI-07 content');
assert(html.includes('SAFE')&&html.includes('Fail safely')&&html.includes('Escalate to a human'),'Missing SAFE framework');
assert(html.includes('ไม่เชื่อมต่อกล้อง')&&html.includes('ไม่ควบคุมปั๊มจริง'),'Missing privacy/device-control limits');
assert(js.includes("arshavin.ai.robots.v1"),'Missing local progress key');
assert(js.includes('ipoComplete')&&js.includes('failsafeComplete')&&js.includes('quizComplete'),'Missing completion gates');
assert(!/(fetch\(|XMLHttpRequest|WebSocket|sendBeacon)/.test(js),'Outbound API found');
assert((ws.match(/class="worksheet"/g)||[]).length===2,'Worksheet must have exactly two pages');
assert(ws.includes('@page')&&ws.includes('A4 portrait'),'Missing A4 print rule');
assert(teacher.includes('Rubric 4 ระดับ')&&teacher.includes('ห้ามต่อปั๊ม'),'Missing rubric or safety guidance');
for(const path of [lesson,script,worksheet,guide])assert(sw.includes(`./${path}`),`Offline cache missing ${path}`);
assert(index.includes(lesson)&&index.includes("arshavin.ai.robots.v1"),'Homepage integration missing');
assert(shell.includes("id: 'AI-07'")&&shell.includes("arshavin.ai.robots.v1"),'Learning shell integration missing');
console.log('AI-07 focused static checks passed');