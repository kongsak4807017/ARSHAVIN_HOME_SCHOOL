import fs from 'node:fs';
import path from 'node:path';
import vm from 'node:vm';
import assert from 'node:assert/strict';
const root=path.resolve(import.meta.dirname,'..');
const read=file=>fs.readFileSync(path.join(root,file),'utf8');
const lesson='subjects/environment/floods-droughts-community-resilience.html';
const script='assets/js/flood-drought-resilience-lesson.js';
const worksheet='worksheets/student/floods-droughts-community-resilience-a4.html';
const guide='worksheets/teacher-guides/floods-droughts-community-resilience-guide.html';
const files=[lesson,script,worksheet,guide];
files.forEach(file=>assert.ok(fs.existsSync(path.join(root,file)),file));
new vm.Script(read(script),{filename:script});
const html=read(lesson),js=read(script),sheets=read(worksheet),teacher=read(guide),all=html+js+sheets+teacher;
assert.match(html,/<html lang="th">/);
assert.match(html,/data-current-lesson="ENV-08"/);
assert.match(all,/RIVER/);
assert.match(all,/flood|น้ำท่วม/i);
assert.match(all,/drought|ภัยแล้ง/i);
assert.match(all,/hazard|ภัย/i);
assert.match(all,/resilience|ยืดหยุ่น/i);
assert.match(all,/ไม่เก็บ.*ที่อยู่|no.*address/is);
assert.match(all,/ไม่.*พิกัด|no.*precise.*location/is);
assert.match(all,/ไม่พาเด็ก.*น้ำหลาก|not.*send.*children.*flood/is);
assert.match(js,/arshavin\.environment\.resilience\.v1/);
['riskComplete','planComplete','quizComplete'].forEach(key=>assert.match(js,new RegExp(key)));
assert.doesNotMatch(js,/fetch\(|XMLHttpRequest|WebSocket|sendBeacon/);
assert.equal((sheets.match(/class="worksheet(?:\s|")/g)||[]).length,2);
assert.match(sheets,/@page\s*\{[^}]*size\s*:\s*A4/is);
assert.doesNotMatch(all,/draggable="true"/);
const sw=read('service-worker.js');
assert.match(sw,/arshavin-grade4-v\d+/);
files.forEach(file=>assert.ok(sw.includes(`./${file}`),file));
const home=read('index.html'),shell=read('assets/js/learning-shell.js');
assert.ok(home.includes(lesson));
assert.ok(home.includes('arshavin.environment.resilience.v1'));
assert.ok(shell.includes("id: 'ENV-08'"));
assert.ok(shell.includes('arshavin.environment.resilience.v1'));
console.log('PASS ENV-08 focused static checks');