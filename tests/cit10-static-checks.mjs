import fs from 'node:fs';
import path from 'node:path';
import vm from 'node:vm';
import assert from 'node:assert/strict';
const root=path.resolve(import.meta.dirname,'..');
const read=file=>fs.readFileSync(path.join(root,file),'utf8');
const lesson='subjects/citizenship/community-citizenship-action-capstone.html';
const script='assets/js/community-citizenship-capstone-lesson.js';
const worksheet='worksheets/student/community-citizenship-action-capstone-a4.html';
const guide='worksheets/teacher-guides/community-citizenship-action-capstone-guide.html';
const material=read(lesson)+read(worksheet)+read(guide);
new vm.Script(read(script),{filename:script});
assert.match(read(lesson),/<html lang="th">/);
assert.match(read(lesson),/data-learning-shell data-current-lesson="CIT-10"/);
assert.match(material,/VOICE/);
assert.match(material,/evidence|หลักฐาน/i);
assert.match(material,/dialogue|สนทนา/i);
assert.match(material,/public institution|หน่วยงานสาธารณะ/i);
assert.match(material,/fair|ความเป็นธรรม/i);
assert.match(material,/budget|งบ/i);
assert.match(material,/survey|แบบสำรวจ/i);
assert.match(material,/risk communication|การสื่อสารความเสี่ยง/i);
assert.match(material,/trade-off/i);
assert.match(material,/political profiling|ความคิดเห็นทางการเมือง/i);
assert.match(material,/ไม่รับคำร้องจริง|not a real petition/i);
assert.match(material,/child-only escalation|เพียงลำพัง/i);
assert.match(material,/ข้อมูลรวม|aggregate/i);
assert.equal((read(worksheet).match(/class="worksheet(?:\s|")/g)||[]).length,2);
assert.match(read(worksheet),/@page\s*\{[^}]*size:A4 portrait/);
['canvasComplete','reviewComplete','quizComplete','arshavin.citizenship.capstone.v1'].forEach(token=>assert.match(read(script),new RegExp(token)));
assert.doesNotMatch(read(script),/fetch\(|XMLHttpRequest|WebSocket|sendBeacon/);
assert.doesNotMatch(material,/draggable="true"|<input[^>]+type="file"/);
const sw=read('service-worker.js');
[lesson,script,worksheet,guide].forEach(file=>assert.ok(sw.includes(`./${file}`),file));
assert.match(sw,/arshavin-grade4-v\d+/);
console.log('PASS CIT-10 focused static checks');