import fs from 'node:fs';
import path from 'node:path';
import vm from 'node:vm';
import assert from 'node:assert/strict';
const root=path.resolve(import.meta.dirname,'..');
const read=f=>fs.readFileSync(path.join(root,f),'utf8');
const lesson='subjects/citizenship/media-public-information-evidence-checking.html';
const script='assets/js/media-evidence-lesson.js';
const worksheet='worksheets/student/media-public-information-evidence-checking-a4.html';
const guide='worksheets/teacher-guides/media-public-information-evidence-checking-guide.html';
const files=[lesson,script,worksheet,guide];
for(const f of files)assert.ok(fs.existsSync(path.join(root,f)),f);
new vm.Script(read(script),{filename:script});
const h=read(lesson),j=read(script),w=read(worksheet),g=read(guide),sw=read('service-worker.js'),index=read('index.html'),shell=read('assets/js/learning-shell.js');
assert.match(h,/<html lang="th">/);
assert.match(h,/data-current-lesson="CIT-03"/);
assert.match(h,/TRACE before sharing|TRACE ก่อนส่งต่อ/);
assert.match(h,/สิ่งที่รู้|Known/);
assert.match(h,/สิ่งที่ยังไม่รู้|Unknown/);
assert.match(h,/สถานการณ์สมมติ/);
assert.match(h,/ไม่ค้น ไม่เผยแพร่ ไม่ติดต่อบุคคลนั้น/);
assert.match(h,/role="status" aria-live="polite"/);
assert.doesNotMatch(h,/draggable="true"/);
assert.doesNotMatch(j,/fetch\(|XMLHttpRequest|WebSocket|sendBeacon/);
['sourceComplete','noticeComplete','quizComplete','arshavin.citizenship.media.v1'].forEach(x=>assert.match(j,new RegExp(x.replaceAll('.','\\.'))));
assert.equal((w.match(/class="worksheet(?:\s|")/g)||[]).length,2);
assert.match(w,/@page\{size:A4 portrait/);
assert.match(g,/Rubric 4 ระดับ/);
assert.match(g,/ไม่ให้เด็กสืบค้นบุคคล/);
for(const f of files)assert.ok(sw.includes(`./${f}`),`offline ${f}`);
assert.match(sw,/arshavin-grade4-v\d+/);
assert.ok(index.includes(lesson));
assert.ok(index.includes('arshavin.citizenship.media.v1'));
assert.ok(shell.includes("id: 'CIT-03'"));
assert.ok(shell.includes('arshavin.citizenship.media.v1'));
console.log('PASS CIT-03 focused static checks');