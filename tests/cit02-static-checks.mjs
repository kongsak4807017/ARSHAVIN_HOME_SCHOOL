import fs from 'node:fs';
import vm from 'node:vm';
import assert from 'node:assert/strict';
const read=p=>fs.readFileSync(p,'utf8');
const lessonPath='subjects/citizenship/community-rules-shared-decisions.html';
const scriptPath='assets/js/community-decisions-lesson.js';
const worksheetPath='worksheets/student/community-rules-shared-decisions-a4.html';
const guidePath='worksheets/teacher-guides/community-rules-shared-decisions-guide.html';
for(const p of [lessonPath,scriptPath,worksheetPath,guidePath])assert.ok(fs.existsSync(p),`missing ${p}`);
const lesson=read(lessonPath),script=read(scriptPath),worksheet=read(worksheetPath),guide=read(guidePath),index=read('index.html'),shell=read('assets/js/learning-shell.js'),sw=read('service-worker.js');
new vm.Script(script,{filename:scriptPath});
assert.match(lesson,/<html lang="th">/);
assert.match(lesson,/Community, Rules and Shared Decisions/);
assert.match(lesson,/data-current-lesson="CIT-02"/);
assert.match(lesson,/vote/i);
assert.match(lesson,/consensus/i);
assert.match(lesson,/รับฟังทุกคน/);
assert.match(lesson,/สถานการณ์จำลอง/);
assert.match(lesson,/ไม่ใช้ชื่อจริง/);
assert.match(lesson,/aria-live="polite"/);
assert.ok((lesson.match(/<fieldset>/g)||[]).length>=6);
assert.doesNotMatch(lesson+script,/dragstart|draggable="true"/);
assert.match(script,/arshavin\.citizenship\.community\.v1/);
assert.match(script,/ruleComplete/);
assert.match(script,/decisionComplete/);
assert.match(script,/quizComplete/);
assert.doesNotMatch(script,/fetch\(|XMLHttpRequest|WebSocket|sendBeacon/);
assert.equal((worksheet.match(/class="worksheet(?:\s|\")/g)||[]).length,2);
assert.match(worksheet,/@page\{size:A4/);
assert.match(guide,/Rubric 4 ระดับ/);
assert.match(guide,/ไม่เปิดเผยเหตุการณ์ขัดแย้งจริง/);
for(const p of [lessonPath,scriptPath,worksheetPath,guidePath])assert.ok(sw.includes(`./${p}`),`offline precache missing ${p}`);
assert.match(sw,/arshavin-grade4-v12/);
assert.ok(index.includes(lessonPath)&&index.includes('arshavin.citizenship.community.v1'));
assert.ok(shell.includes("id: 'CIT-02'")&&shell.includes('ruleComplete && data.decisionComplete && data.quizComplete'));
console.log('CIT-02 focused static checks passed.');
