import fs from 'node:fs';
import vm from 'node:vm';
const read=p=>fs.readFileSync(new URL(`../${p}`,import.meta.url),'utf8');
const assert=(ok,msg)=>{if(!ok)throw new Error(msg)};
const lessonPath='subjects/maker-engineering/pulleys-gears-transfer-force.html';
const scriptPath='assets/js/pulleys-gears-lesson.js';
const worksheetPath='worksheets/student/pulleys-gears-transfer-force-a4.html';
const guidePath='worksheets/teacher-guides/pulleys-gears-transfer-force-guide.html';
for(const p of [lessonPath,scriptPath,worksheetPath,guidePath])assert(fs.existsSync(new URL(`../${p}`,import.meta.url)),`missing ${p}`);
const lesson=read(lessonPath),script=read(scriptPath),worksheet=read(worksheetPath),guide=read(guidePath),index=read('index.html'),shell=read('assets/js/learning-shell.js'),sw=read('service-worker.js');
new vm.Script(script,{filename:scriptPath});
assert(/<html lang="th">/.test(lesson),'Thai document language missing');
assert(/Pulleys, Gears and Transferring Force/.test(lesson),'English title missing');
assert(/data-current-lesson="MAKER-02"/.test(lesson),'shell id missing');
assert((lesson.match(/<fieldset>/g)||[]).length>=6,'native fieldsets missing');
assert(/aria-live="polite"/.test(lesson),'polite feedback missing');
assert(/type="range"/.test(lesson)&&/<select/.test(lesson),'keyboard-native explorers missing');
assert(!/(dragstart|draggable="true")/.test(lesson+script),'drag-only interaction found');
assert(/arshavin\.maker\.pulleysgears\.v1/.test(script),'local progress key missing');
assert(/try\{localStorage/.test(script)&&/catch\{\}/.test(script),'guarded storage missing');
assert(!/(fetch\(|XMLHttpRequest|WebSocket|sendBeacon)/.test(script),'outbound network API found');
assert((worksheet.match(/class="worksheet"/g)||[]).length===2,'worksheet must contain exactly two A4 pages');
assert(/@page\{size:A4/.test(worksheet),'A4 print rule missing');
for(const phrase of ['ห้ามยกคน','จุดหนีบ','ผู้ใหญ่เป็นผู้ตัด'])assert((lesson+guide).includes(phrase),`safety phrase missing: ${phrase}`);
for(const path of [lessonPath,scriptPath,worksheetPath,guidePath])assert(sw.includes(`./${path}`),`offline precache missing ${path}`);
assert(sw.includes("arshavin-grade4-v11"),'cache v11 missing');
assert(index.includes(lessonPath)&&index.includes('arshavin.maker.pulleysgears.v1'),'homepage integration missing');
assert(shell.includes("id: 'MAKER-02'")&&shell.includes('arshavin.maker.pulleysgears.v1'),'shell integration missing');
assert(/แบบจำลองอุดมคติ/.test(lesson+worksheet+guide),'ideal-model boundary missing');
console.log('MAKER-02 focused static checks passed.');