import fs from 'node:fs';
import vm from 'node:vm';
const read=p=>fs.readFileSync(new URL(`../${p}`,import.meta.url),'utf8');
const lesson=read('subjects/ai-science/ai-decisions-confidence-human-escalation.html');
const script=read('assets/js/ai-confidence-lesson.js');
