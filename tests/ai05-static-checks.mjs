const index=read('index.html');
const assert=(ok,msg)=>{if(!ok)throw new Error(msg);};
new vm.Script(script);
assert(lesson.includes('AI-05')&&lesson.includes('Confidence')&&lesson.includes('ความมั่นใจ'),'bilingual AI-05 title missing');
assert(lesson.includes('abstain')&&lesson.includes('human escalation')&&lesson.includes('threshold'),'core vocabulary missing');
assert(lesson.includes('aria-live="polite"')&&lesson.includes('<fieldset>')&&lesson.includes('<select'),'accessible native controls missing');
assert(!/fetch\s*\(|XMLHttpRequest|WebSocket|sendBeacon/.test(script),'outbound API found');
assert(script.includes("arshavin.ai.confidence.v1")&&script.includes('simulatorComplete')&&script.includes('stakesComplete')&&script.includes('quizComplete'),'guarded completion state missing');
