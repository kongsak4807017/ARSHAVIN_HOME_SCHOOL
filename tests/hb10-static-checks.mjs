import fs from 'node:fs';
import path from 'node:path';
import assert from 'node:assert/strict';

const root = path.resolve(import.meta.dirname, '..');
const read = file => fs.readFileSync(path.join(root, file), 'utf8');
const lessonPath = 'subjects/human-body/hormones-growth-respectful-health-information.html';
const scriptPath = 'assets/js/hormones-growth-lesson.js';
const worksheetPath = 'worksheets/student/hormones-growth-respectful-health-information-a4.html';
const guidePath = 'worksheets/teacher-guides/hormones-growth-respectful-health-information-guide.html';
const files = [lessonPath, scriptPath, worksheetPath, guidePath];
for (const file of files) assert.ok(fs.existsSync(path.join(root, file)), `missing ${file}`);
const lesson = read(lessonPath);
const script = read(scriptPath);
const worksheet = read(worksheetPath);
const guide = read(guidePath);
const combined = `${lesson}\n${script}\n${worksheet}\n${guide}`;

assert.match(lesson, /HB-10/);
assert.match(lesson, /Hormones, Growth and Respectful Health Information/);
assert.match(combined, /gland[\s\S]*hormone[\s\S]*blood[\s\S]*target[\s\S]*response/i);
assert.match(combined, /chemical messenger|สารสื่อสารทางเคมี/i);
assert.match(combined, /ไม่ทำนาย|does not predict|cannot predict/i);
assert.match(combined, /ไม่เปรียบเทียบ|do not compare|avoid.*comparison|ไม่จัดอันดับ/i);
assert.match(combined, /ส่วนสูง|height/i);
assert.match(combined, /น้ำหนัก|weight/i);
assert.match(combined, /ความเป็นส่วนตัว|privacy/i);
assert.match(combined, /ผู้ใหญ่|trusted adult|health professional/i);
assert.match(script, /arshavin\.humanbody\.hormones\.v1/);
assert.match(script, /signalComplete/);
assert.match(script, /respectComplete/);
assert.match(script, /quizComplete/);
assert.doesNotMatch(script, /fetch\s*\(|XMLHttpRequest|WebSocket|sendBeacon/);
assert.match(lesson, /fieldset/);
assert.match(lesson, /legend/);
assert.match(lesson, /aria-live="polite"/);
assert.match(lesson, /tabindex="-1"/);
assert.doesNotMatch(combined, /dragstart|draggable="true"/);
assert.match(worksheet, /@page\{size:A4 portrait/);
assert.equal((worksheet.match(/class="worksheet"/g) || []).length, 2);
assert.match(worksheet, /page-break-after|break-after/);
assert.match(guide, /Rubric 4 ระดับ/);
assert.match(guide, /ไม่มี timer|no timer/i);
assert.match(guide, /ไม่.*จำกัดอาหาร|does not promote restrictive eating|ไม่ส่งเสริมการจำกัดอาหาร/i);
console.log('HB-10 focused static checks passed.');
