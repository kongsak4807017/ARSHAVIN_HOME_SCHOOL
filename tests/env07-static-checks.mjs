import fs from 'node:fs';
import vm from 'node:vm';
const read=p=>fs.readFileSync(new URL(`../${p}`,import.meta.url),'utf8');
const assert=(condition,message)=>{if(!condition)throw new Error(message);};
const lesson='subjects/environment/ecosystems-biodiversity-responsible-observation.html';
const script='assets/js/ecosystems-biodiversity-lesson.js';
const worksheet='worksheets/student/ecosystems-biodiversity-responsible-observation-a4.html';
const guide='worksheets/teacher-guides/ecosystems-biodiversity-responsible-observation-guide.html';
for(const path of [lesson,script,worksheet,guide])assert(fs.existsSync(new URL(`../${path}`,import.meta.url)),`Missing ${path}`);
const html=read(lesson),js=read(script),ws=read(worksheet),teacher=read(guide),sw=read('service-worker.js'),index=read('index.html'),shell=read('assets/js/learning-shell.js');
const material=html+js+teacher;
new vm.Script(js);
assert(html.includes('ENV-07')&&html.includes('Ecosystems, Biodiversity and Responsible Observation'),'Missing bilingual ENV-07 content');
assert(html.includes('CARE')&&html.includes('Avoid disturbance')&&html.includes('Record responsibly'),'Missing CARE framework');
assert(html.includes('producer')&&html.includes('consumer')&&html.includes('decomposer'),'Missing food-web roles');
assert(material.includes('ไม่บันทึกหรือเผยแพร่พิกัด')||material.includes('ไม่เผยพิกัด'),'Missing precise wildlife-location protection');
assert(/ไม่ต้องทำ fieldwork จริง|no fieldwork/.test(material),'Missing no-risky-fieldwork boundary');
assert(js.includes("arshavin.environment.biodiversity.v1"),'Missing local progress key');
assert(js.includes('foodwebComplete')&&js.includes('habitatComplete')&&js.includes('quizComplete'),'Missing completion gates');
assert(!/(fetch\(|XMLHttpRequest|WebSocket|sendBeacon)/.test(js),'Outbound API found');
assert((ws.match(/class="worksheet"/g)||[]).length===2,'Worksheet must have exactly two pages');
assert(ws.includes('@page')&&ws.includes('A4 portrait'),'Missing A4 print rule');
assert(teacher.includes('Rubric 4 ระดับ')&&teacher.includes('ไม่จับ ไม่ให้อาหาร'),'Missing rubric or observation safety guidance');
for(const path of [lesson,script,worksheet,guide])assert(sw.includes(`./${path}`),`Offline cache missing ${path}`);
assert(index.includes(lesson)&&index.includes("arshavin.environment.biodiversity.v1"),'Homepage integration missing');
assert(shell.includes("id: 'ENV-07'")&&shell.includes("arshavin.environment.biodiversity.v1"),'Learning shell integration missing');
console.log('ENV-07 focused static checks passed');
