import fs from 'node:fs';
import assert from 'node:assert/strict';

const read = path => fs.readFileSync(path, 'utf8');
const lessonPath = 'subjects/human-body/body-privacy-boundaries-consent-trusted-help.html';
const scriptPath = 'assets/js/body-privacy-consent-lesson.js';
const worksheetPath = 'worksheets/student/body-privacy-boundaries-consent-trusted-help-a4.html';
const guidePath = 'worksheets/teacher-guides/body-privacy-boundaries-consent-trusted-help-guide.html';
for (const path of [lessonPath, scriptPath, worksheetPath, guidePath]) assert.ok(fs.existsSync(path), `missing ${path}`);
const lesson = read(lessonPath), script = read(scriptPath), worksheet = read(worksheetPath), guide = read(guidePath);
assert.match(lesson, /HB-11/);
assert.match(lesson, /STOP/);
assert.match(lesson, /consent|การยินยอม/i);
assert.match(lesson, /trusted adult|ผู้ใหญ่ที่ไว้ใจ/i);
assert.match(lesson, /ไม่ต้องเล่าเหตุการณ์จริง|No personal disclosure/i);
assert.match(lesson + guide, /ไม่บังคับ|ไม่ต้องเล่า|no personal disclosure/i);
assert.match(lesson + guide, /ความเงียบไม่ใช่|silence is not consent/i);
assert.match(lesson + guide, /เปลี่ยนใจได้|changeable/i);
assert.match(lesson + guide, /บอกผู้ใหญ่.*ต่อ|another trusted adult|keep telling/i);
assert.match(lesson + guide, /ไม่ซักรายละเอียดเกินจำเป็น|minimal|เกินจำเป็น/i);
assert.match(lesson, /fieldset/);
assert.match(lesson, /legend/);
assert.match(lesson, /aria-live="polite"/);
assert.match(script, /arshavin\.humanbody\.bodyprivacy\.v1/);
assert.match(script, /boundaryComplete/);
assert.match(script, /helpComplete/);
assert.match(script, /quizComplete/);
assert.doesNotMatch(script, /fetch\(|XMLHttpRequest|WebSocket|sendBeacon/);
assert.equal((worksheet.match(/class="worksheet"/g) || []).length, 2);
assert.match(worksheet, /@page\{size:A4 portrait/);
assert.match(worksheet, /page-break-after/);
assert.match(guide, /Rubric 4 ระดับ/);
assert.match(guide, /AAC/);
assert.match(guide, /ไม่มี timer|without.*timer/i);
assert.doesNotMatch(lesson + script, /draggable=|dragstart|ondrag/);
console.log('HB-11 focused static checks passed.');