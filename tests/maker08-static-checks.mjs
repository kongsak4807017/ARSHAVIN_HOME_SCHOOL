import fs from 'node:fs';
import path from 'node:path';
import vm from 'node:vm';
import assert from 'node:assert/strict';
const root=path.resolve(import.meta.dirname,'..');
const read=file=>fs.readFileSync(path.join(root,file),'utf8');
const lesson='subjects/maker-engineering/simple-circuits-energy-electrical-safety.html';
const script='assets/js/simple-circuits-lesson.js';
const worksheet='worksheets/student/simple-circuits-energy-electrical-safety-a4.html';
const guide='worksheets/teacher-guides/simple-circuits-energy-electrical-safety-guide.html';
const files=[lesson,script,worksheet,guide];
files.forEach(file=>assert.ok(fs.existsSync(path.join(root,file)),file));
new vm.Script(read(script),{filename:script});
const html=read(lesson),js=read(script),sheets=read(worksheet),teacher=read(guide),all=html+js+sheets+teacher;
assert.match(html,/<html lang="th">/);
assert.match(html,/data-current-lesson="MAKER-08"/);
assert.match(all,/วงจรเปิด|open circuit/i);
assert.match(all,/วงจรปิด|closed circuit/i);
assert.match(all,/source|แหล่งพลังงาน/i);
assert.match(all,/switch|สวิตช์/i);
assert.match(all,/load|อุปกรณ์ใช้พลังงาน/i);
assert.match(all,/ลัดวงจร|short circuit/i);
assert.match(all,/ห้าม.*ไฟบ้าน|no mains electricity/is);
assert.match(all,/แจ้งผู้ใหญ่|alert an adult/i);
assert.match(js,/arshavin\.maker\.circuits\.v1/);
['modelComplete','designComplete','quizComplete'].forEach(key=>assert.match(js,new RegExp(key)));
assert.doesNotMatch(js,/fetch\(|XMLHttpRequest|WebSocket|sendBeacon/);
assert.equal((sheets.match(/class="worksheet(?:\s|")/g)||[]).length,2);
assert.match(sheets,/@page\s*\{[^}]*size\s*:\s*A4/is);
assert.doesNotMatch(all,/draggable="true"/);
const sw=read('service-worker.js');
assert.match(sw,/arshavin-grade4-v\d+/);
files.forEach(file=>assert.ok(sw.includes(`./${file}`),file));
const home=read('index.html'),shell=read('assets/js/learning-shell.js');
assert.ok(home.includes(lesson));
assert.ok(home.includes('arshavin.maker.circuits.v1'));
assert.ok(shell.includes("id: 'MAKER-08'"));
assert.ok(shell.includes('arshavin.maker.circuits.v1'));
console.log('PASS MAKER-08 focused static checks');